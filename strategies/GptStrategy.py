    def populate_indicators(self, dataframe: DataFrame, metadata: dict) -> DataFrame:
        """
        Adds several different TA indicators to the given DataFrame

        Performance Note: For the best performance be frugal on the number of indicators
        you are using. Let uncomment only the indicator you are using in your strategies
        or your hyperopt configuration, otherwise you will waste your memory and CPU usage.
        :param dataframe: Dataframe with data from the exchange
        :param metadata: Additional information, like the currently traded pair
        :return: a Dataframe with all mandatory indicators for the strategies
        """
        


        # MACD
        macd = ta.MACD(dataframe)
        dataframe['macd'] = macd['macd']
        dataframe['macdsignal'] = macd['macdsignal']
        dataframe['macdhist'] = macd['macdhist']


        # Get the volume data
        volume_data = dataframe['volume'].values.tolist()

        # Initialize an empty list for the indicator
        indicator = []

        # Loop through the volume data and calculate the indicator
        for i in range(len(volume_data)):
            # If there are less than 20 data points, the indicator is False
            if i < 20:
                indicator.append(False)
            else:
                # Otherwise, calculate the indicator
                indicator.append(self.is_big_amount(volume_data[i-19:i+1]))

        # Add the indicator to the dataframe
        dataframe['big_amount'] = indicator

        return dataframe

    def is_big_amount(self, volume_data):
        # 最近20天的成交量计算
        scaler = MinMaxScaler()
        volume_data20 = volume_data[-20:]
        normalized_volume_list = scaler.fit_transform([[x] for x in volume_data20])
        normalized_volume_list = [x[0] for x in normalized_volume_list]

        y_pred = self.loaded_svm.predict([normalized_volume_list])

        return y_pred[0] == 1
    
